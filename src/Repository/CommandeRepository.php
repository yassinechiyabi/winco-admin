    public function getAllCommandes():Array{
        $conn = $this->getEntityManager()->getConnection();

        $sql = '
        select commande.id,commande.Etat,nomClient,prenomClient,email,phone,nom_plan,prix_mensuel_plan,prix_miseEnLigne_plan ,domaine,nomSite,typeSite from commande
         inner join client on client.id=commande.id_client 
         inner join plan on plan.id=commande.id_plan
            ';

        $resultSet = $conn->executeQuery($sql);

        // returns an array of arrays (i.e. a raw data set)
        return $resultSet->fetchAllAssociative();
    }

